def get_lesson(track: str, level_key: str) -> list:
    """Возвращает список упражнений для заданного трека и уровня.
    Пример: get_lesson("beginner", "2_home_row")
    :return: список строк, например ["f j", "s l", ...]
    """
    return LESSONS.get(track, {}).get(level_key, [])
